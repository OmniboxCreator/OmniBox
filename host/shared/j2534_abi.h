
#ifndef J2534_ABI_H
#define J2534_ABI_H

#include <stdint.h>

#define J2534_DATA_MAX 4128

typedef struct {
  uint32_t ProtocolID;
  uint32_t RxStatus;
  uint32_t TxFlags;
  uint32_t Timestamp;
  uint32_t DataSize;
  uint32_t ExtraDataIndex;
  uint8_t Data[J2534_DATA_MAX];
} PASSTHRU_MSG;


enum {
  J2534_J1850VPW = 0x01, J2534_J1850PWM = 0x02, J2534_ISO9141 = 0x03,
  J2534_ISO14230 = 0x04, J2534_CAN = 0x05, J2534_ISO15765 = 0x06,
  J2534_CAN_PS = 0x8004, J2534_ISO15765_PS = 0x8005,
  J2534_SW_ISO15765_PS = 0x8007, J2534_SW_CAN_PS = 0x8008,
};


enum {
  J2534_STATUS_NOERROR = 0x00, J2534_ERR_NOT_SUPPORTED = 0x01,
  J2534_ERR_INVALID_CHANNEL_ID = 0x02, J2534_ERR_INVALID_PROTOCOL_ID = 0x03,
  J2534_ERR_NULL_PARAMETER = 0x04, J2534_ERR_TIMEOUT = 0x09,
  J2534_ERR_INVALID_MSG = 0x0A, J2534_ERR_BUFFER_EMPTY = 0x10,
  J2534_ERR_BUFFER_FULL = 0x11, J2534_ERR_BUFFER_OVERFLOW = 0x12,
  J2534_ERR_DEVICE_NOT_CONNECTED = 0x14, J2534_ERR_FAILED = 0x16,
  J2534_ERR_DEVICE_NOT_OPEN = 0x18,
};


enum { J2534_PASS_FILTER = 1, J2534_BLOCK_FILTER = 2, J2534_FLOW_CONTROL_FILTER = 3 };


enum {
  J2534_GET_CONFIG = 0x01, J2534_SET_CONFIG = 0x02, J2534_READ_VBATT = 0x03,
  J2534_FIVE_BAUD_INIT = 0x04, J2534_FAST_INIT = 0x05,
  J2534_CLEAR_TX_BUFFER = 0x07, J2534_CLEAR_RX_BUFFER = 0x08,
  J2534_CLEAR_PERIODIC_MSGS = 0x09, J2534_CLEAR_MSG_FILTERS = 0x0A,
  J2534_READ_PROG_VOLTAGE = 0x0E,
};


enum {
  J2534_CFG_DATA_RATE = 0x01, J2534_CFG_LOOPBACK = 0x03,
  J2534_CFG_ISO15765_BS = 0x1E, J2534_CFG_ISO15765_STMIN = 0x1F,
  J2534_CFG_BS_TX = 0x22, J2534_CFG_STMIN_TX = 0x23,
};


#define J2534_VOLTAGE_OFF   0xFFFFFFFFu
#define J2534_SHORT_TO_GROUND 0xFFFFFFFEu


#define J2534_TX_MSG_TYPE    0x00000001u
#define J2534_ISO15765_FRAME_PAD 0x00000040u
#define J2534_CAN_29BIT_ID    0x00000100u


typedef struct { uint32_t Parameter; uint32_t Value; } SCONFIG;
typedef struct { uint32_t NumOfParams; SCONFIG *ConfigPtr; } SCONFIG_LIST;
typedef struct { uint32_t NumOfBytes; uint8_t *BytePtr; } SBYTE_ARRAY;

#endif 
